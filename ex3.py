# +: plus
# -: minus
# /: slash
# *: asterisk
# %: percent (modulus)
# <: less-than
# >: greater-than
# <=: less-than-equal
# >=: greater-than-equal

print("I will now count my chickens:") #prints the string

print("Hens", 25 + 30 / 6) #prints Hens and the answer to the math (30)
print("Roosters", 100 - 25 * 3 % 4) #prints Roosters and the answer (97)

print("Now I will count the eggs:") #prints the string

print(3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6) #prints the math answer ()

print("Is it true that 3 + 2 < 5 - 7?") #prints everything as a string

print(3 + 2 < 5 - 7) #prints a boolean (False)

print("What is 3 + 2?", 3 + 2) #prints the quotes as a string and 5
print("What is 5 - 7?", 5 - 7) #prints the quotes as a string and -2

print("Oh, that's why it's False.") #prints the string

print("How about some more.") #prints the string

print("Is it greater?", 5 > -2) # prints string and True
print("Is it greater or equal?", 5>= -2) #prints True
print("Is it less or equal?", 5 <= -2) #prints false